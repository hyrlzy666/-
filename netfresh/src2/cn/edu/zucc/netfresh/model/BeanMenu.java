package cn.edu.zucc.netfresh.model;

public class BeanMenu {
	private String menuId;
	private String menuName;
	private String menuCommodity;
	private String menuStep;
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuCommodity() {
		return menuCommodity;
	}
	public void setMenuCommodity(String menuCommodity) {
		this.menuCommodity = menuCommodity;
	}
	public String getMenuStep() {
		return menuStep;
	}
	public void setMenuStep(String menuStep) {
		this.menuStep = menuStep;
	}
	
}
